          <p class="mt-10 mb-5 text-left">With everyone applying for jobs online these days, companies receive more applications than they can read. If you are applying for a job, your application will likely land in an applicant tracking system (ATS). This software is used to keep track of applications and ensure none are overlooked. It also automatically sorts and filters resumes. For recruiters and HR, an ATS-optimized resume means they can quickly identify the most qualified applicants. For you, it can mean that your resume is seen by the right people</p>
          <p class=" mb-5 text-left">The use of automation and assessment systems in recruiting will continue to grow as software begins to leverage AI. Therefore, an ATS-optimized resume is essential to get past the bots and in front of a human recruiter</p>
          <p class=" mb-5 text-left">If you are worried about getting your resume past the ATS, don’t be. Follow these simple tips for an ATS-friendly resume, and you’ll create a resume that impresses the hiring manager and passes through the ATS with ease</p>
           <h1 class="text-gray-900 text-center text-2xl font-bold mb-14">Use keywords to win over the bots</h1>
          <p class="mb-10 text-left">To ensure an ATS reads your resume (known as resume parsing), it’s essential to optimize it with the right keywords. Keywords represent the qualifications, skills, and expertise you have that qualify you for the job you want. Using the right keywords can ensure that your resume will be seriously considered</p>

          
          <div class="text-left">
  <h2 class="mb-2 text-lg font-semibold text-gray-900 dark:text-white">Here’s how to optimize your resume for ATS</h2>
  <l class="space-y-1 text-gray-600 text-end list-disc dark:text-gray-400">
    <li >Read the job posting and look for keywords to include in your resume.</li>
    <li>Include the name of the company and the exact job title in your resume. You can do this in your cover letter, summary, or career objective section.</li>
    <li>Include any education, certifications, or licenses relevant to the job you are applying for.</li>
    <li>Distribute the keywords throughout the "Summary", "Education", "Experience", and "Skills" sections, and use them in your cover letter as well.</li>
    <li>Review your resume for the correct spelling of keywords and proper use of abbreviations (as used in the job posting).</li>
  </l>
</div>

<br> 

          <ol>
              <li class="mb-5 text-justify"><strong>Don’t automatically save your resume as a PDF file</strong>PDF files are not always the most compatible file type for applicant tracking systems when it comes to resumes. While PDF files are great for preserving the design and format of your resume template, they may not work with all ATS software.If you are asked to upload your resume to an ATS in PDF format, you should do so. However, unless PDF compatibility is explicitly stated, submitting your resume as a Word document (.doc or .docx) or as plain text is better. This way, you will not have any formatting issues.</li>
              <li class="mb-5 text-justify"><strong>Write for the person who will eventually see it </strong>When writing your resume, keep in mind that it’s not just an ATS that will read it. Once your resume has gone through the ATS, a recruiter will review it and decide if you are a suitable candidate. Therefore, while it is important to use ATS keywords in your resume, you should also make sure that your resume is readable and honest. This means being truthful about your skills and experience and not stuffing your resume with keywords.</li>
              <li class="mb-5 text-justify"><strong>Hold back on the creativity</strong>When it comes to creating a resume that stands out, sometimes less is more. A simple, clean design is often easier for an applicant tracking system to read and quicker for recruiters to skim. Resumes that are too cluttered or have an unusual format can be confusing and frustrating for those who review them.</li>
              
          </ol>
          
          <h1 class="mb-4 text-left text-3xl font-extrabold leading-tight text-gray-900 lg:mb-6 lg:text-3xl dark:text-white">More tips for an ATS friendly resume</h1>
          <p class="mb-10 text-left">:To ensure your resume makes it through an ATS, follow these tips</p>
          <ol>
              <li class="mb-5 text-justify">Write a unique version: don’t just use the same old resume template that everyone else is using. Take the time to personalize your resume and make it stand out.</li>
              <li class="mb-5 text-justify">Edit the text to improve the quality. Make sure it does not contain grammatical or spelling errors and that your language is clear and concise.</li>
              <li class="mb-5 text-justify">Avoid using images, graphs, tables, and charts, as these elements are difficult for an applicant tracking system to see and read. Instead, focus on providing clear and concise information that the system can easily understand.</li>
              <li class="mb-5 text-justify">It’s important to use keywords relevant to the industry and job you are applying for, but avoid jargon or buzzwords that only a specific group of people would understand. Common buzzwords like “self-starter” and “hard worker” are overused in resumes.</li>
              <li class="mb-5 text-justify">When using a resume template, make sure your contact information is in the resume’s body and not in the header or footer. Many applicant tracking systems cannot correctly read and understand the information in these areas, so your contact information may be lost.</li>
              <li class="mb-5 text-justify">If you want your resume to stand out, bullet points are a great way to highlight your accomplishments and qualifications. However, using an overly complicated symbol for your bullet points can make your key selling points difficult to understand. Stick to more straightforward options, such as a solid circle, an open circle, or a square, to ensure that your bullet points enhance your resume rather than detract from it.</li>
          </ol>
          
          <h1 class="mb-4 text-left text-3xl font-extrabold leading-tight text-gray-900 lg:mb-6 lg:text-3xl dark:text-white">Final thoughts</h1>
          <p class="mb-10 text-justify">Many recruiters still prefer to go through every application entered into their applicant tracking system. In most cases, they only take a quick look at your past accomplishments, job titles, and companies you have worked for. Within six seconds, they have already decided whether they want to know more about you or not. Therefore, your key skills and qualifications must be visible and easy to identify.</p>
          <p class="mb-10 text-justify">It’s important that your resume stands out for ATS software, but you also need to be careful that you do not give the impression of trying to “trick” the system with an ATS-optimized resume. Instead, focus on showing your best side on your resume. It’s all about striking a balance between being functional and informative.</p>
