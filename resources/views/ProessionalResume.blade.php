          <p class="mt-10 mb-5 text-left">Your resume needs to be professional and polished because, if not, your application materials probably won't get a second glance from any hiring manager</p>
          <p class=" mb-5 text-left">An unprofessional resume—one that is difficult to read, confusing, covered in errors, or unrelated to the job the person is applying for—will get tossed in the trash right away. Hiring managers often get dozens, even hundreds, of applicants for each job. An unprofessional resume makes you look unprofessional as a job seeker and will cost you a possible interview</p>
          <p class=" mb-5 text-left">Sloppy resumes that are riddled with typos will be ignored and resumes that are inconsistent —bullets in some places, dashes in others, bold in some headings, plain text in others—may not get a second look either</p>
          <div class="flex flex-1">
                <div class="inline-block mb-11  h-32 w-0.5 self-stretch bg-cyan-700 opacity-100 dark:opacity-50"></div>
                 <h1 class="pr-6 font-bold text-2xl ">Note</h1>
                 <p class="mt-14 ml-12 text-left ">Your resume, to be effective, needs to be consistent, concise, and clear and easy to read. Avoid tiny fonts, dense blocks of text, vague language or excessive jargon, and inconsistent formatting</p>
                    </div>
                    <h1 class="text-gray-900 text-center text-2xl font-bold mb-14">How to Create a Professional Resume</h1>
          <p class="mb-10 text-left">Grab your current resume (or organize your work experience and education information) and give it a professional boost with these tips:</p>

          <ol>
              <li class="mb-5 text-left"><strong>Select the Best Resume Type</strong>: There are several basic types of resumes used to apply for job openings. Depending on your personal circumstances, choose a chronological, functional, combination, or a targeted resume. Taking the time to choose the best type of resume for your situation is well worth the effort</li>
              <li class="mb-5 text-left"><strong>Choose a Simple Font</strong>: Your resume should be easy to read. You want the hiring manager to easily read and absorb your work history and accomplishments. Therefore, use a legible font (such as Times New Roman, Arial, or Calibri). Make sure the font is not too big or too small (choose a size between 10 and 12).Avoid dense blocks of text and use standard margins. Use white- or cream-colored paper if you are sending a physical resume; colored paper can be very distracting</li>
              <li class="mb-5 text-left"><strong>Keep Your Formatting Consistent</strong>: Professional resumes need to have consistent formatting. For instance, if you use bullet points to describe your responsibilities and achievements at one position, be sure to use bullet points in all other positions as well.Also, make certain that the bullet points are formatted the same way throughout. For example, don't use circle bullet points in one section, and diamond bullet points in another section. Be consistent with font, font size, and style (such as the use of bold and italics)</li>
              <li  class="mb-5 text-left"><strong>Keep it Focused</strong>: It's important not to include extraneous information. More isn’t necessarily better. Your resume should focus on the skills and attributes that qualify you for the job. It will be helpful to leave out anything that won’t help you get the job you want.A resume shouldn’t be several pages long for the average job seeker, a one-page resume is probably enough, or two pages at most</li>
              <li class="mb-5 text-left"><strong>Give It a Makeover</strong>: It's always a good idea to periodically refresh your resume. Be sure you have included all your latest achievements, and your employment and educational history are current. Updating your resume will increase your chances of getting it noticed by the hiring manager</li>
              <li class="mb-5 text-left"><strong>Use Resume Examples and Templates</strong>:Use a resume example or template to help you write your resume. An example can help you decide what information to include. Templates can help you format your resume. However, whenever you use a resume example or template, be sure to customize your resume, so it reflects your skills and abilities, and the jobs you are applying for. A simple copy/paste isn’t enough</li>
              <li class="mb-5 text-left"><strong>Get Creative (Maybe)</strong>: If you’re in a creative field, you might use a free resume website to build a creative resume that includes all the facets of a traditional resume, with add-ons like video, infographics, and links to your accomplishments. However, only do this if you are in a creative industry. Otherwise, you definitely need to stick to a traditional resume</li>
              <li class="mb-5 text-left"><strong>Carefully Edit Your Resume</strong>: Spelling and grammar errors can make an applicant seem inattentive to details. Review proofing guidelines to ensure that your resume is consistent and error-free. Then check it again. And, if you can, find someone else to look at it too, because it’s so easy to miss your own typos</li>
              <li class="mb-5 text-left"><strong>Get Resume Help</strong>: Writing a resume is hard work and it's important to get help, or at least have your resume reviewed before you send it to employers. Consider using a career counselor or other professional resume service to help you make sure your resume is professional and polished. Having a friend or family member check for typos and formatting errors is another option to make sure it's perfect</li>
          </ol>
          
