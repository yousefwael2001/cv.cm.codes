<br> 

          <ol>
              <li class="mb-5 text-justify"><strong>1. Use the proper format and structure</strong><br>Before you start writing your student CV, it is essential to determine the correct structure and format. This gives your CV a professional look and improves legibility. Use simple and clear fonts that make it easy to read your CV.</li>
              <li class="mb-5 text-justify"><strong>2. Study the position and the employer</strong><br>Researching the employer and the position can help you determine the education qualifications and skills to include on your CV. If you're applying for graduate studies, look at the requirements of the program and study the CV of other candidates. You can also check job descriptions in your industry to identify the common skills employers want to see on your CV.</li>
              <li class="mb-5 text-justify"><strong>3. Start with a captivating personal statement or objective</strong><br>Lead with a compelling personal statement that highlights your skills, achievements and educational qualifications. Your CV personal statement comprises two to three sentences summarizing who you are, your academic qualifications and experience, your ambitions and what you will bring to a position or an employer. Even if you have no work experience, you can use this section to highlight relevant skills, voluntary service and coursework that makes you an ideal candidate.</li>
              <li class="mb-5 text-justify"><strong>4. Be creative with your education</strong><br>Students often lack extensive work experience to include in their CV. With creativity, you can expand your education section to increase the amount of detail in the document. Things you can add are relevant subjects, coursework, projects, awards and grades if they will improve your chances of getting the role.
              <p class="mt-10 mb-5 text-justify">Start from your most recent educational qualification in reverse chronological order. If you have a higher degree, you can drop your high school credentials from the CV. Add your GPA only if it will boost your chance of getting the role, preferably something between 3.5 to 4.0. Mention your education first if you have no work experience.</p>
              <p class="mt-10 mb-5 text-justify">When listing schools, include the name of the program, school and year attended. Optional information includes your GPA, honors, minor and academic achievements. If you're an undergraduate, you can list essential courses and modules relevant to the role. You can also include your expected degree classification and previous grades if they improve your candidacy.</p></li>
              <li class="mb-5 text-justify"><strong>5. Expand your work experience</strong><br>Even if you have no paid work experience, you can include volunteer work, placements, internships and team activities in your CV. These activities can help you highlight workplace skills and demonstrate your passion for the position.</p>
              <p class="mt-10 mb-5 text-justify">In this section, start from your most recent role. Include voluntary work, internship placements and all full- and part-time paid work relevant to the role. Each position needs to include the company name, job title and the months and years that you worked.</p>
              <p class="mt-10 mb-5 text-justify">In your key responsibilities section, highlight the skills, experience and achievements you gained at each role. Add only the responsibilities relevant to the job you're applying for and quantify your accomplishments with figures. Additionally, make sure there are no gaps in your work history.</p></li>
              <li class="mb-5 text-justify"><strong>6. Add other sections</strong><br>If you need to highlight your skills, an additional skills section allows you to mention hard, soft and general competencies and your level of proficiency. You can also include achievements such as awards and recognition and positions of responsibility at school. Make sure to include only real skills and accomplishments because the hiring manager may ask you to prove them at the interview.</p>
              <p class="mt-10 mb-5 text-justify">You can also add a hobby and interests section in your student CV, that concisely lists potentially-relevant skills and experience. Hobbies and interests that demonstrate a love for learning or determination can show a hiring manager transferable capabilities.</p>
              <p class="mt-10 mb-5 text-justify">Other optional sections you can add to a student CV include certificates and awards, languages and personal projects.</p></li>

              <li class="mb-5 text-justify"><strong>7. Proofread and edit</strong><br>After writing your CV, proofread and edit the document multiple times before sending it to the employer. You may have excellent credentials, but typos and grammatical errors can send the wrong impression to recruiters. Many free online tools will check your CV for grammar and style errors. You might also consider having another person review your CV for feedback on how to improve the content and quality.</li>

          </ol>

